#include "../includes/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	t_uchar	*d;

	d = (t_uchar *)s;
	i = 0;
	while (i < n)
	{
		if (d[i] == (t_uchar)c)
			return (d + i);
		i++;
	}
	return (0);
}
